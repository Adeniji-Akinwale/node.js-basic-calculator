export function numAdd(a,b){
    return `The sum of ${a}+${b} = ${a+b} `
}


export function numSubtract(a,b){
    return `The Subtraction of ${a}-${b} = ${a-b}`
}

export function numDivide(a,b){
   return `The Division of ${a}/${b} = ${a/b}`
}

export function numMultiply(a,b){
   return `The Multiplication of ${a}*${b} = ${a*b}`
}
